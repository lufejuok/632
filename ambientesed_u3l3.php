                      <br><br>
                      Las dimensiones del desarrollo humano también nos llevan a pensar que, si bien cada ser humano conjuga en su ser estas dimensiones de forma integral, en cada uno-a de nosotros-as tiene mayor fortaleza una o varias dimensiones. Algunas-nos tenemos mayores condiciones para el lenguaje, otros en cambio para los aspectos físicos o corporales, mientras que otras-os tenemos mayores potenciales en la dimensión estética, creativa, lúdica. De allí que es importante que, independientemente de las áreas de conocimiento que lideremos, nuestras formas de orientar el aprendizaje logren vincular diferentes lenguajes en clave de las dimensiones del desarrollo humano, logrando con ello corresponder a la diversidad de lo humano y dar con aprendizajes significativos. 
