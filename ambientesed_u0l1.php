                    <p>El interés en los ambientes educativos diferenciales está puesto en lograr procesos de aprendizaje que proporcionen una vivencia del desarrollo humano, a partir de la reflexión de los factores bio-químicos, físicos y psicosociales que intervienen en todo acto educativo. 
                    <br>      Los ambientes educativos como parte constitutiva de las prácticas pedagógicas, nos desafían a observar autocríticamente las interacciones que ocurren entre los escenarios, los actores y los contenidos, como los componentes principales que los integran.  
                    <br>      Todo ello con la finalidad de dimensionar la complejidad que tiene lugar en la vida pedagógica cotidiana, y en especial como motivación a imaginar ambientes educativos que fortalezcan los intercambios entre estudiantes y profesoras-es, encaminados a la generación de aprendizajes significativos desde el reconocimiento de las diferencias. 
                    <br>      El curso concibe los ambientes educativos bajo una perspectiva relacional, que integra tanto los espacios físicos, las interacciones docentes-estudiantes y estudiantes-estudiantes propias del entorno escolar, así como la interacción con otros contextos sociales más amplios, como son la familia, el barrio, la comunidad, la ciudad, e incluso escenarios intangibles como las redes y los contenidos digitales. 
                    <br>      Esta perspectiva relacional nos invita a comprender la educación como un contínuum en que se expresan diferentes escenarios, actores y contenidos de forma dinámica, interdependiente y en permanente transformación, que hacen de las prácticas pedagógicas un campo de constante expresión de la diversidad. 
                    <br>      Por este motivo los ambientes educativos diferenciales están llamados a conjugar condiciones para el logro de resultados de aprendizaje orientados al saber, como a promover aprendizajes sentidos para el saber ser-con-otros-otras. 
                    <br>      Metodológicamente, el curso tiene la firme intención de aportar a los procesos cotidianos de las-os docentes, con lo cual esperamos este propicie la reflexión pedagógica, inspire la imaginación metodológica y promueva la articulación de saberes y complicidades para el enriquecimiento de los ambientes educativos, en los que ustedes, al decir de Paulo Freire, son los-as protagonistas de los inéditos posibles de la educación transformadora. 

                    </p>
             
