                    <h3>Presentación    </h3> 
                    <p>El concepto de escenario, nos abre la posibilidad de comprender la educación como un proceso que ocurre en diferentes espacios sociales, y nos desafía a pensar las oportunidades de la pedagogía más allá de las fronteras escolares y del aula. Hechos recientes como la emergencia por covid-19, nos pusieron de manifiesto lo que ya sabíamos, pero que tal vez no teníamos tan consciente, esto es, la profundización de la digitalización de la vida como resultado de la masificación de las nuevas tecnologías informáticas y de las comunicaciones NTIC. Que, al igual que las TIC de generaciones anteriores (telegrama, telefonía, radio, televisión), transformaron los modos de relacionamiento y nuestras representaciones sociales mismas, introduciendo no sólo nuevos escenarios, interacciones y formas de recrear contenidos educativos, sino que también transformaron las formas mismas de aprender, hasta el punto que algunos estudios plantean que hemos sufrido incluso cambios en nuestras estructuras biológicas.  
                      <br><br>
                      Esto ha dado lugar a nuevos problemas, oportunidades y desafíos, los cuales replantean nuestra forma de concebir los ambientes educativos, así como la educación misma, de ahí las tensiones y muchas de las encrucijadas que cotidianamente experimentamos en nuestro rol pedagógico. 
                      <br><br>
                      Tal realidad afirma la complejidad que tiene todo proceso educativo, al tiempo que nos invita a comprender los ambientes educativos como los escenarios que "en pequeño", expresan las dinámicas macro de la vida en sociedad.
