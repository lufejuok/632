                      <br>
                      El Aprendizaje basado en proyectos -ABPr es una de las metodologías activas, que busca implementar un conjunto de tareas basadas en la resolución de preguntas o problemas (retos), mediante un proceso de investigación o creación por parte de estudiantes, que trabajan de manera relativamente autónoma y con un alto nivel de implicación y cooperación. El proceso culmina con un producto final, presentado ante los demás (difusión), propiciando el aprendizaje significativo, mediante el intercambio cultural, en múltiples dimensiones, propendiendo por una educación más activa. 
                      <br>
                      Los laboratorios en el ámbito educativo, están concebidos para que los educadores puedan incorporar la investigación en educación, como un elemento que les permita identificar de manera científica, los hallazgos que se suscitan en el ámbito de los encuentros con sus estudiantes, permite socializar el conocimiento a través de escritos tipo artículos, ponencias o publicaciones en general (blogs, web sites, boletines).
                      <br>
                      La gamificación es una metodología activa de aprendizaje, basada en el juego; su objetivo es conseguir mejores resultados entre los estudiantes, a través de dinámicas que les impliquen y les motiven a progresar. Las herramientas de gamificación, buscan una interiorización de los conceptos dinámica, personalizada y con la que los estudiantes consigan ser los protagonistas de su propia evolución académica. 

                    
