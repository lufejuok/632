                    <p>A continuación te invitamos a leer el cuento de los Hermanos Grimm escrito en el año 1697, Caperucita Roja. Identifica detalles, palabras o expresiones del cuento que sean nuevas para ti y relaciónalas con la importancia de cuidar de ti mismo o misma.</p>
                    <p>Érase una vez una niñita que lucía una hermosa capa de color rojo. Como la niña la usaba muy a menudo, todos la llamaban Caperucita Roja.
                    <br>Un día, la mamá de Caperucita Roja la llamó y le dijo:
                    <br>—Abuelita no se siente muy bien, he horneado unas galleticas y quiero que tú se las lleves.
                    <br>—Claro que sí —respondió Caperucita Roja, poniéndose su capa y llenando su canasta de galleticas recién horneadas.
                    <br>Antes de salir, su mamá le dijo:
                    <br>— Escúchame muy bien, quédate en el camino y nunca hables con extraños.
                    <br>—Yo sé mamá —respondió Caperucita Roja y salió inmediatamente hacia la casa de la abuelita.
                    <br>Para llegar a casa de la abuelita, Caperucita debía atravesar un camino a lo largo del espeso bosque. En el camino, se encontró con el lobo.
                    <br>—Hola niñita, ¿hacia dónde te diriges en este maravilloso día? —preguntó el lobo.
                    <br>Caperucita Roja recordó que su mamá le había advertido no hablar con extraños, pero el lobo lucía muy elegante, además era muy amigable y educado.
                    <br>—Voy a la casa de abuelita, señor lobo —respondió la niña—. Ella se encuentra enferma y voy a llevarle estas galleticas para animarla un poco.
                    <br>—¡Qué buena niña eres! —exclamó el lobo. —¿Qué tan lejos tienes que ir?
                    <br>—¡Oh! Debo llegar hasta el final del camino, ahí vive abuelita—dijo Caperucita con una sonrisa.
                    <br>—Te deseo un muy feliz día mi niña —respondió el lobo.
                    <br>El lobo se adentró en el bosque. Él tenía un enorme apetito y en realidad no era de confiar. Así que corrió hasta la casa de la abuela antes de que Caperucita pudiera alcanzarlo. Su plan era comerse a la abuela, a Caperucita Roja y a todas las galleticas recién horneadas.
                    <br>El lobo tocó la puerta de la abuela. Al verlo, la abuelita corrió despavorida dejando atrás su chal. El lobo tomó el chal de la viejecita y luego se puso sus lentes y su gorrito de noche. Rápidamente, se trepó en la cama de la abuelita, cubriéndose hasta la nariz con la manta. Pronto escuchó que tocaban la puerta:
                    <br>—Abuelita, soy yo, Caperucita Roja.
                    <br>Con vos disimulada, tratando de sonar como la abuelita, el lobo dijo:
                    <br>—Pasa mi niña, estoy en camita.
                    <br>Caperucita Roja pensó que su abuelita se encontraba muy enferma porque se veía muy pálida y sonaba terrible.
                    <br>—¡Abuelita, abuelita, qué ojos más grandes tienes!
                    <br>—Son para verte mejor —respondió el lobo.
                    <br>—¡Abuelita, abuelita, qué orejas más grandes tienes!
                    <br>—Son para oírte mejor —susurró el lobo.
                    <br>—¡Abuelita, abuelita, que dientes más grandes tienes!
                    <br>—¡Son para comerte mejor!
                    <br>Con estas palabras, el malvado lobo tiró su manta y saltó de la cama.
                    <br>Asustada, Caperucita salió corriendo hacia la puerta. Justo en ese momento, un leñador se acercó a la puerta, la cual se encontraba entreabierta. La abuelita estaba escondida detrás de él.
                    <br>Al ver al leñador, el lobo saltó por la ventana y huyó espantado para nunca ser visto.
                    <br>La abuelita y Caperucita Roja agradecieron al leñador por salvarlas del malvado lobo y todos comieron galleticas con leche. Ese día Caperucita Roja aprendió una importante lección:
                    <br>“Nunca debes hablar con extraños”. (Caperucita Roja, 1697).
                    </p>
