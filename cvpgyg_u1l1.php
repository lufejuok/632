                  <h3>INTRODUCCIÓN</h3> 
                
                  <p>Uno de los temas más ampliamente debatidos por las sociedades durante los últimos años, son las relaciones de género y generación. Alrededor de estas, se teje un sinfín de imaginarios que se insertan en los currículos y se cuelan en las conversaciones cotidianas que, en entornos como la escuela, se desarrollan, para tratar de comprender los roles que les son adjudicados a estudiantes y docentes, según ciertas características socialmente construidas. Es responsabilidad de la escuela, por consiguiente, avanzar hacia objetivos que promuevan un sistema que garantice el acceso y la permanencia de las y los estudiantes, a través de oportunidades diferenciadas, centradas en una educación flexible, adaptada a los contextos, a los diversos códigos de comunicación entre los sujetos, facilitadora del respeto y, a su vez, garante del dominio de las tecnologías de la información y la comunicación, en virtud de las múltiples capacidades de docentes y estudiantes (Perales, 2012).   
                  Abordar las relaciones de género y generación, en contextos escolares, sugiere la inclusión de principios como la acción colaborativa, la relación teoría-práctica y el aprender haciendo.  En cuanto a la acción colaborativa, se propone como estrategia destinada a crear ambientes de apertura y solidaridad, los cuales permitan recuperar las experiencias de individuos y comunidades, para empoderarles en la formulación participativa de propuestas pedagógicas visibilizadoras de la diversidad. En el mismo sentido, la relación teoría-práctica y el aprender haciendo, se presentan como opciones para lograr la congruencia de elementos conceptuales y metodológicos, relacionados con la reflexión y la acción sobre el entorno.   <br><br>
                
