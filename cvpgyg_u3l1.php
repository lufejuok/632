                      <p> <b>Introducción:</b> a partir del recorrido por la conceptualización de la perspectiva de género y de generación, se propone replantear el lugar de la familia y de la escuela como máximos referentes de autoridad, de forma que se transformen en escenarios para promover relaciones éticas y políticas que sean construidas y compartidas horizontalmente, esto implica una mirada intergeneracional.  Tal como lo plantea la Política Pública de Infancias y Adolescencias de Medellín, el enfoque intergeneracional:  <br><br>
